    // Remove the selected column from table
    private void btnExcluirGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirGrupoActionPerformed
        String selectedColumnName = table.getColumnName(table.getSelectedColumn());
        table.removeColumn(table.getColumn(selectedColumnName));
        for (int i=0; i>table.getModel().getRowCount(); i++) {
            table.getModel().setValueAt(0, i, table.getSelectedColumn());
        }
    }//GEN-LAST:event_btnExcluirGrupoActionPerformed

    
